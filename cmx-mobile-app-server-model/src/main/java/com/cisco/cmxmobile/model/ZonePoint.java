package com.cisco.cmxmobile.model;

public class ZonePoint {

    private float pointX;

    private float pointY;

    private float pointZ;

    public void setX(float x) {
        pointX = x;
    }

    public float getX() {
        return pointX;
    }

    public void setY(float y) {
        pointY = y;
    }

    public float getY() {
        return pointY;
    }

    public void setZ(float z) {
        pointZ = z;
    }

    public float getZ() {
        return pointZ;
    }
}
